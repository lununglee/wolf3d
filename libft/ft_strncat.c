#include "libft.h"

char		*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	size_t	i;
	size_t	j;

	i = ft_strlen(s1);
	j = -1;
	while (s2[++j] && (j < n))
		*(s1 + i + j) = *(s2 + j);
	*(s1 + i + j) = '\0';
	return (s1);
}
